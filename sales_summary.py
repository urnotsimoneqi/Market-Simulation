class SalesSummary:
    def __init__(self, seller_id, sales_year, sales_quarter, sales_expenses_amount, sales_revenue, sales_profit):
        self.seller_id = seller_id
        self.sales_year = sales_year
        self.sales_quarter = sales_quarter
        self.sales_expenses_amount = sales_expenses_amount
        self.sales_revenue = sales_revenue
        self.sales_profit = sales_profit
